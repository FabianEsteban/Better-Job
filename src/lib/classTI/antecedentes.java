package lib.classTI;

public class antecedentes {
	
	
	
	public int ID_ante;
	public String rut_usuario_ante;
	public String cargo;
	public String institucion;
	public String anos;
	public String descripcion;
	
	
	
	public int getID_ante() {
		return ID_ante;
	}
	public void setID_ante(int iD_ante) {
		ID_ante = iD_ante;
	}
	public String getRut_usuario_ante() {
		return rut_usuario_ante;
	}
	public void setRut_usuario_ante(String rut_usuario_ante) {
		this.rut_usuario_ante = rut_usuario_ante;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getInstitucion() {
		return institucion;
	}
	public void setInstitucion(String institucion) {
		this.institucion = institucion;
	}
	public String getAnos() {
		return anos;
	}
	public void setAnos(String anos) {
		this.anos = anos;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
}
